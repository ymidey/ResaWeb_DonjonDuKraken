<h1>À propos</h1>

<h2>Qui suis-je ?</h2>
<p>Je m'appelle Yannick Midey et je suis étudiant en première année de BUT MMI à l'IUT de Champs-Sur-Marne, j'ai réalisé
    ce site de réservation dans le cadre d'un projet tutoré de second semestre nommé resaweb.<br> J'ai fait le choix de
    créer une
    plateforme de réservation d'événement de jeux de société car j'ai dévéloppé un atrait particulier pour les jeux de
    société notamment les escapes-games. Cependant dans cette récente passion, je me suis appercu de deux principaux
    probèmes, le prix des jeux de société et un nombre de joueur conséquent requis pour de nombreux jeux.</p>

<p>Cette plateforme permet donc de résoudre ses deux problèmes, en effet, elle permet à n'importe qui de tester de
    nombreux jeux différents et variés, à un coût réduit, mais aussi de pouvoir jouer avec un grand nombre de joueur
</p>

